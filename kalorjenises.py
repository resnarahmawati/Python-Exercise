# KALOR JENIS ES
# Q = m × c × t

print('''
=============================
       KALOR JENIS ES
=============================
''')

m = float(input('Masukan massa benda:'))
c = float(input('Masukankalor jenis es (dalam Joule per kilogram per derajat Celsius) :'))
t = float(input('Masukan perubahan suhu es:'))
Q =  m * c * t

print('KALOR JENIS ES:',Q)